	<p><strong>Машина <nobr>АДМ-1,5Б исп.</nobr> 6.1 (на&nbsp;базе мотовоза АДМ)</strong></p>
<p>Предназначена для разработки котлованов под установку железобетонных опор контактной сети на&nbsp;железных дорогах; выполнения погрузочно-разгрузочных работ с&nbsp;помощью грузоподъемного крана; маневровых работ; перевозки рабочих бригад; питания&nbsp;эл. энергией потребителей в&nbsp;полевых условиях.</p>
