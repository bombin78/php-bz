<p><strong>Преимущества бурового рабочего органа:</strong></p>
<ol>
    <li>Заглубление бура происходит на&nbsp;всю глубину котлована за&nbsp;счет увеличенной длины шнека, поэтому отпадает необходимость периодического заглубления-выглубления бура и&nbsp;разбрасывания грунта на&nbsp;повышенной скорости вращения. </li>
    <li>Повышенная скорость вращения бура (125&nbsp;об/мин) при разработке котлована позволяет избегать постоянной очистки шнека от&nbsp;грунта. </li>
    <li>Регулирование подачи бура позволяет изменять величину подачи при разработке котлована в&nbsp;зависимости от&nbsp;категории грунта. </li>

</ol>
