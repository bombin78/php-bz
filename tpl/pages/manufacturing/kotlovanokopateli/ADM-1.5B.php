	<p><strong>Машина <nobr>АДМ-1,5Б исп.</nobr> 6.1 (на базе мотовоза АДМ)</strong></p>
<p>Предназначена для разработки котлованов под установку железобетонных опор контактной сети на железных дорогах; выполнения погрузочно-разгрузочных работ с помощью грузоподъемного крана; маневровых работ; перевозки рабочих бригад; питания эл. энергией потребителей в полевых условиях.</p>
