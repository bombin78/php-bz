<p><strong>Преимущества бурового рабочего органа:</strong></p>
<ol>
    <li>Заглубление бура происходит на всю глубину котлована за счет увеличенной длины шнека, поэтому отпадает необходимость периодического заглубления-выглубления бура и разбрасывания грунта на повышенной скорости вращения. </li>
    <li>Повышенная скорость вращения бура (125 об/мин) при разработке котлована позволяет избегать постоянной очистки шнека от грунта. </li>
    <li>Регулирование подачи бура позволяет изменять величину подачи при разработке котлована в зависимости от категории грунта. </li>

</ol>
